/**
 * El primer int es el ingreso del numero A entero
 * El segundo int es el ingreso del numero B entero
 *
 * return int retorna el resultado de la suma
 */
int sumaDeNumeros(int, int);

/**
 * El primer int es el ingreso del numero A entero
 * El segundo int es el ingreso del numero B entero
 *
 * return int retorna el resultado de la resta
 */
int restaDeNumeros(int, int);

/**
 * El primer int es el ingreso del numero A entero en funcion se convierte en float
 * El segundo int es el ingreso del numero B entero en funcion se convierte en float
 *
 * retorna dos tipos de printf dependiendo el caso de los numero ingresados
 */
void divisionDeNumeros(int, int);

/**
 * El primer int es el ingreso del numero A entero en funcion
 * El segundo int es el ingreso del numero B entero en funcion
 *
 * retorna producto de la multiplicacion
 */
int multiplicacionDeNumeros(int, int);

/**
 * El int es el ingreso del numero entero
 *
 * retorna el resultado de la operacion
 */
int factorialDeUnNumero(int);
